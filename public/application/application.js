angular
	.module('sej',['ngRoute'])
	.config(function($routeProvider){
	    $routeProvider
		    .when('/',{
		        templateUrl:'views/login.html',
		        controller:'login'
		    })
		    .when('/password',{
		    	templateUrl:'views/password.html',
		    	controller:'password'
		    })
		    .when('/logout',{
		    	templateUrl:'views/logout.html',
		    	controller:'logout'
		    })
		    .when('/acercade',{
		    	templateUrl:'views/acercade.html',
		    	controller:'acercade'
		    })
		    .when('/mdi',{
		        templateUrl:'views/mdi.html',
		        controller:'mdi'
		    })
		    .when('/login',{
		        templateUrl:'views/login.html',
		        controller:'login'
		    })
		    .when('/escuelas',{
		    	templateUrl:'views/escuelas.html',
		    	controller:'escuelas'
		    })
		    .otherwise({redirectTo:'/logout'});
	});